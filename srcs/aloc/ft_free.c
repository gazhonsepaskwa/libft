#include "../../libft.h"

void	ft_free(char **p)
{
	if (p && *p)
	{
		free(*p);
		*p = NULL;
	}
}

void	ft_free_v(void **p)
{
	if (p && *p)
	{
		free(*p);
		*p = NULL;
	}
}
